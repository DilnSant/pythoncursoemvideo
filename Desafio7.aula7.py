# Ler 2 notas de um aluno, calcule e mostre a média:
nota1 = float(input('Qual sua primeira nota?'))
nota2 = float(input('Qual a segunda nota?'))
media = (nota1 + nota2) / 2
print('Sua primeira nota foi:', nota1, 'e a segunda nota:', nota2)
#print('Sendo assim, sua média é: {} + {} / 2'.format(nota1, nota2))
print('Sendo assim, sua média é:', media)




